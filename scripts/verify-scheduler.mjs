import { execFileSync } from "node:child_process";
import { mkdirSync } from "node:fs";
import { pathToFileURL } from "node:url";

mkdirSync(".tmp/scheduler-check", { recursive: true });

const tscArgs = [
    "tsc",
    "--outDir",
    ".tmp/scheduler-check",
    "--module",
    "NodeNext",
    "--moduleResolution",
    "NodeNext",
    "--target",
    "ES2022",
    "--skipLibCheck",
    "lib/autoScheduler.ts",
    "lib/schedule.ts",
    "lib/seedData.ts",
    "lib/types.ts",
];

if (process.platform === "win32") {
  execFileSync("cmd.exe", ["/c", "node_modules\\.bin\\tsc.cmd", ...tscArgs.slice(1)], { stdio: "inherit" });
} else {
  execFileSync("node_modules/.bin/tsc", tscArgs.slice(1), { stdio: "inherit" });
}

const [{ autoPlanSchedule, plannerCapacityWarning }, { calculateCourseProgress, projectedCourseFinishDate }, { seedData }] = await Promise.all([
  import(pathToFileURL(`${process.cwd()}/.tmp/scheduler-check/autoScheduler.js`)),
  import(pathToFileURL(`${process.cwd()}/.tmp/scheduler-check/schedule.js`)),
  import(pathToFileURL(`${process.cwd()}/.tmp/scheduler-check/seedData.js`)),
]);

const fixtureState = {
  ...seedData,
  settings: {
    ...seedData.settings,
    startDate: "2026-06-15",
  },
};

const planned = autoPlanSchedule(fixtureState, { today: "2026-06-15", maxDailyHours: 8 });
const tasks = planned.tasks;
const courses = new Map(planned.courses.map((course) => [course.id, course]));
const errors = [];
const lateTasks = [];

const dailyHours = new Map();
for (const task of tasks) {
  dailyHours.set(task.date, (dailyHours.get(task.date) ?? 0) + task.estimatedHours);
}

for (const [date, hours] of dailyHours) {
  if (hours > 12) {
    errors.push(`${date} has ${hours}h scheduled, which indicates overload clustering.`);
  }
}

const generatedLecturesByCourse = new Map();
for (const task of tasks.filter((item) => item.autoGenerated && item.type === "lecture")) {
  if (!task.courseId) continue;
  const course = courses.get(task.courseId);
  if (!course) {
    errors.push(`Generated task ${task.title} references missing course ${task.courseId}.`);
    continue;
  }
  if (task.date < "2026-06-15") errors.push(`${task.title} was scheduled before today.`);
  if (task.date > course.targetFinishDate) lateTasks.push(`${task.title} after ${course.name}'s finish date`);

  const previousDate = generatedLecturesByCourse.get(task.courseId);
  if (previousDate && task.date < previousDate) {
    errors.push(`${course.name} lectures are out of order around ${task.title}.`);
  }
  generatedLecturesByCourse.set(task.courseId, task.date);
}

for (const task of tasks.filter((item) => item.autoGenerated && (item.type === "revision" || item.type === "mock"))) {
  const course = task.courseId ? courses.get(task.courseId) : undefined;
  if (!course) continue;
  const deadline = course.examDate ?? course.targetFinishDate;
  if (task.date > deadline) lateTasks.push(`${task.title} after its deadline`);
}

const examKeys = new Set();
for (const task of tasks.filter((item) => item.type === "exam")) {
  const key = `${task.courseId}:${task.date}`;
  if (examKeys.has(key)) errors.push(`Duplicate exam task for ${key}.`);
  examKeys.add(key);
}

const roomyWarning = plannerCapacityWarning(fixtureState, { today: "2026-06-15", maxDailyHours: 24 });
if (roomyWarning) {
  errors.push(`Capacity warning should not appear when the current 24h cap can fit the plan. Got: ${roomyWarning.message}`);
}

const morningWarning = plannerCapacityWarning(fixtureState, {
  today: "2026-06-15",
  now: new Date("2026-06-15T08:00:00"),
  maxDailyHours: 8,
});
const eveningWarning = plannerCapacityWarning(fixtureState, {
  today: "2026-06-15",
  now: new Date("2026-06-15T23:00:00"),
  maxDailyHours: 8,
});
if ((eveningWarning?.suggestedDailyLimit ?? 0) > (morningWarning?.suggestedDailyLimit ?? 0)) {
  errors.push("Capacity warning is still inflated by the time remaining today.");
}

const mathOneProgress = calculateCourseProgress(fixtureState, "2026-06-15").find((course) => course.id === "math-1");
if (mathOneProgress?.planningTargetDate === "2026-06-15") {
  errors.push("Expired priority buffer still shows the course as due today.");
}

