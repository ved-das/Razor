function planRangeMiss(state: StudyState) {
  const rangeEnd = state.settings?.planEndDate;
  if (!rangeEnd) return undefined;
  const latestScheduledDate = state.tasks
    .filter((task) => task.autoGenerated)
    .map((task) => task.date)
    .sort()
    .at(-1);
  if (!latestScheduledDate || latestScheduledDate <= rangeEnd) return undefined;
  return { rangeEnd, latestScheduledDate };
}

export function validatePlannedState(state: StudyState, today: string): PlannerValidationIssue[] {
  const issues: PlannerValidationIssue[] = [];
  const settings = state.settings;
  const maxDailyHours = settings?.maxDailyHours ?? 8;
  const rules: CapacityRules = {
    today,
    todayCapacity: maxDailyHours,
    holidays: new Set(settings?.holidays ?? []),
    customStudyHours: new Map((settings?.customStudyHours ?? migrateRestrictedDays(settings?.restrictedDays)).map((day) => [day.date, day.hours])),
  };
  const tasks = state.tasks.filter((task) => task.type !== "exam" && task.type !== "rest");
  const tasksByCourse = new Map<string, StudyTask[]>();
  const dailyHours = new Map<string, number>();
  const courseIds = new Set(state.courses.map((course) => course.id));

  for (const graphIssue of dependencyGraphIssues(state.courses)) {
    issues.push({
      type: "dependency",
      severity: "error",
      courseId: graphIssue.courseId,
      message: graphIssue.message,
      blockers: graphIssue.path,
      suggestions: ["Fix the dependency chain before generating a plan."],
    });
  }

  for (const dataIssue of corruptedInputIssues(state, today)) {
    issues.push(dataIssue);
  }

  for (const task of tasks) {
    if (task.courseId && !courseIds.has(task.courseId)) {
      issues.push({
        type: "data",
        severity: "warning",
        date: task.date,
        taskId: task.id,
        courseId: task.courseId,
        message: `${task.title} references a deleted course and will be ignored by course planning.`,
        suggestions: ["Relink the task to an existing course or remove it."],
      });
    }
    dailyHours.set(task.date, roundToOne((dailyHours.get(task.date) ?? 0) + task.estimatedHours));
    if (task.courseId) {
      const courseTasks = tasksByCourse.get(task.courseId) ?? [];
      courseTasks.push(task);
      tasksByCourse.set(task.courseId, courseTasks);
    }
    if (rules.holidays.has(task.date)) {
      issues.push({
        type: "holiday",
        severity: "error",
        date: task.date,
        taskId: task.id,
        courseId: task.courseId,
        message: `${task.title} is scheduled on a holiday (${task.date}).`,
      });
    }
    const taskDeadline = task.customDeadline ?? task.deadline;
    if (taskDeadline && task.date > taskDeadline) {
      issues.push({
        type: "deadline",
        severity: "error",
        date: task.date,
        taskId: task.id,
        courseId: task.courseId,
        message: `${task.title} is scheduled on ${task.date}, after its ${taskDeadline} deadline.`,
      });
    }
  }

  for (const [date, hours] of dailyHours) {
    const capacity = capacityForDate(date, maxDailyHours, today, maxDailyHours, rules);
    if (hours > capacity) {
      issues.push({
        type: "capacity",
        severity: "error",
        date,
        message: `${date} has ${roundToOne(hours)}h planned, above the ${roundToOne(capacity)}h available.`,
      });
    }
  }

  const coreFinishByCourse = new Map<string, string>();
  const coreStartByCourse = new Map<string, string>();
  for (const course of state.courses) {
    const courseTasks = (tasksByCourse.get(course.id) ?? []).sort((a, b) => a.date.localeCompare(b.date) || (a.planningOrder ?? 0) - (b.planningOrder ?? 0));
    const coreTasks = courseTasks.filter((task) => !["revision", "mock"].includes(task.type));
    const coreFinish = coreTasks.map((task) => task.date).sort().at(-1);
    const coreStart = coreTasks.map((task) => task.date).sort().at(0);
    if (coreFinish) coreFinishByCourse.set(course.id, coreFinish);
    if (coreStart) coreStartByCourse.set(course.id, coreStart);

    const effectiveDeadline = course.targetFinishDate ?? (course.examDate ? addDays(course.examDate, -EXAM_PREP_WINDOW_DAYS) : undefined);
    if (effectiveDeadline && coreFinish && coreFinish > effectiveDeadline) {
      issues.push({
        type: "deadline",
        severity: "error",
        date: coreFinish,
        courseId: course.id,
        message: `${course.name} finishes on ${coreFinish}, after its ${effectiveDeadline} target.`,
      });
    }

    const seenMethodDates = new Map<string, string>();
    const seenGeneratedKeys = new Set<string>();
    for (const task of coreTasks) {
      const methodKey = task.studyMethodId ?? (task.type === "lecture" ? "lectures" : task.type);
      const itemKey = `${course.id}:${methodKey}:${task.studyMethodItem ?? task.title}:${task.parentTaskId ?? ""}:${task.segmentNumber ?? ""}`;
      if ((task.autoGenerated || task.origin === "generated") && seenGeneratedKeys.has(itemKey)) {
        issues.push({
          type: "duplicate",
          severity: "error",
          date: task.date,
          taskId: task.id,
          courseId: course.id,
          message: `${task.title} was generated more than once.`,
          suggestions: ["Regenerate the plan after clearing duplicate generated tasks."],
        });
      }
      seenGeneratedKeys.add(itemKey);

      const previousDate = seenMethodDates.get(methodKey);
      if (previousDate && task.date < previousDate) {
        issues.push({
          type: "order",
          severity: "error",
          date: task.date,
          taskId: task.id,
          courseId: course.id,
          message: `${task.title} is out of sequence for ${course.name}.`,
        });
      }
      seenMethodDates.set(methodKey, task.date);
    }

    for (const issue of strictSequenceIssues(course, coreTasks)) {
      issues.push(issue);
    }

    const prepStart = course.examDate ? examPrepStartDate(course.examDate) : undefined;
    for (const task of courseTasks.filter((item) => item.type === "revision" || item.type === "mock")) {
      if (prepStart && task.date < prepStart) {
        issues.push({
          type: "revision",
          severity: "error",
          date: task.date,
          taskId: task.id,
          courseId: course.id,
          message: `${task.title} is before the exam prep window starts on ${prepStart}.`,
        });
      }
      if (task.type === "mock" && coreFinish && task.date <= coreFinish) {
        issues.push({
          type: "revision",
          severity: "error",
          date: task.date,
          taskId: task.id,
          courseId: course.id,
          message: `${task.title} is before ${course.name} core work is complete.`,
        });
      }
    }
  }

  for (const course of state.courses) {
    const start = coreStartByCourse.get(course.id);
    if (!start) continue;
    for (const dependencyId of getDependencyIds(course)) {
      const dependencyFinish = coreFinishByCourse.get(dependencyId);
      if (dependencyFinish && start <= dependencyFinish) {
        issues.push({
          type: "dependency",
          severity: "error",
          date: start,
          courseId: course.id,
          message: `${course.name} starts on ${start} before its prerequisite finishes on ${dependencyFinish}.`,
        });
      }
    }
  }

  return issues.sort(compareIssueSeverity);
}

function planningOrder(course: Course, originalOrder: Map<string, number>) {
  return course.planningOrder ?? originalOrder.get(course.id) ?? 9999;
}

function dependencyGraphIssues(courses: Course[]): DependencyGraphIssue[] {
  const issues: DependencyGraphIssue[] = [];
  const byId = new Map(courses.map((course) => [course.id, course]));

  for (const course of courses) {
    for (const dependencyId of getDependencyIds(course)) {
      if (!byId.has(dependencyId)) {
        issues.push({
          type: "missing",
          courseId: course.id,
          message: `${course.name} depends on a missing or deleted course. Fix the dependency before planning.`,
          path: [course.name, dependencyId],
        });
      }
    }
  }

  const visiting = new Set<string>();
  const visited = new Set<string>();
  const stack: string[] = [];
  const cycleKeys = new Set<string>();

  function visit(courseId: string) {
    if (visiting.has(courseId)) {
      const cycleStart = stack.indexOf(courseId);
      const cycleIds = [...stack.slice(Math.max(cycleStart, 0)), courseId];
      const key = cycleIds.join(">");
      if (!cycleKeys.has(key)) {
        cycleKeys.add(key);
        const path = cycleIds.map((id) => byId.get(id)?.name ?? id);
        const first = cycleIds[0];
        issues.push({
          type: "cycle",
          courseId: first,
          message: `Circular dependency detected: ${path.join(" -> ")}.`,
          path,
          courseIds: cycleIds,
        });
      }
      return;
    }
    if (visited.has(courseId)) return;
    const course = byId.get(courseId);
    if (!course) return;
    visiting.add(courseId);
    stack.push(courseId);
    for (const dependencyId of getDependencyIds(course)) visit(dependencyId);
    stack.pop();
    visiting.delete(courseId);
    visited.add(courseId);
  }

  for (const course of courses) visit(course.id);
  return issues;
}

function corruptedInputIssues(state: StudyState, today: string): PlannerValidationIssue[] {
  const issues: PlannerValidationIssue[] = [];
  const seenCourses = new Set<string>();
  const seenTasks = new Set<string>();

  for (const course of state.courses) {
    if (seenCourses.has(course.id)) {
      issues.push({
        type: "data",
        severity: "error",
        courseId: course.id,
        message: `Duplicate course ID detected: ${course.id}.`,
        suggestions: ["Rename or remove the duplicate course before planning."],
      });
    }
    seenCourses.add(course.id);
    if (course.completedLectures > course.totalLectures) {
      issues.push({
        type: "data",
        severity: "warning",
        courseId: course.id,
        message: `${course.name} has more completed items than total items; progress is clamped for planning.`,
      });
    }
    if (course.targetFinishDate && course.targetFinishDate < today) {
      issues.push({
        type: "deadline",
        severity: "warning",
        courseId: course.id,
        date: course.targetFinishDate,
        message: `${course.name} has a target date in the past.`,
        suggestions: ["Move the target date or generate a rescue plan."],
      });
    }
    if (course.examDate && course.targetFinishDate && course.examDate < course.targetFinishDate) {
      issues.push({
        type: "deadline",
        severity: "warning",
        courseId: course.id,
        date: course.examDate,
        message: `${course.name} has an exam before its target finish date.`,
        suggestions: ["Move the target finish date before the exam date."],
      });
    }
  }

  for (const task of state.tasks) {
    if (seenTasks.has(task.id)) {
      issues.push({
        type: "duplicate",
        severity: "error",
        taskId: task.id,
        date: task.date,
        message: `Duplicate task ID detected: ${task.id}.`,
        suggestions: ["Remove or regenerate duplicate tasks."],
      });
    }
    seenTasks.add(task.id);
    if (!isDateKey(task.date)) {
      issues.push({
        type: "data",
        severity: "error",
        taskId: task.id,
        message: `${task.title} has an invalid planned date.`,
      });
    }
    if (task.estimatedHours < 0 || !Number.isFinite(task.estimatedHours)) {
      issues.push({
        type: "data",
        severity: "error",
        taskId: task.id,
        message: `${task.title} has invalid hours.`,
      });
    }
  }

  return issues;
}

function strictSequenceIssues(course: Course, coreTasks: StudyTask[]): PlannerValidationIssue[] {
  const issues: PlannerValidationIssue[] = [];
  const byMethod = new Map<string, StudyTask[]>();
  for (const task of coreTasks) {
    const methodKey = task.studyMethodId ?? (task.type === "lecture" ? "lectures" : task.type);
    const tasks = byMethod.get(methodKey) ?? [];
    tasks.push(task);
    byMethod.set(methodKey, tasks);
  }

  for (const [methodKey, tasks] of byMethod) {
    const method = getStudyMethods(course).find((item) => item.id === methodKey);
    const type = method?.taskType ?? (methodKey === "lectures" ? "lecture" : tasks[0]?.type);
    if (!type || taskOrderMode(type) !== "strict") continue;

    const ordered = [...tasks]
      .filter((task) => typeof task.studyMethodItem === "number")
      .sort((a, b) => (a.studyMethodItem ?? 0) - (b.studyMethodItem ?? 0));
    for (let index = 1; index < ordered.length; index += 1) {
      const previous = ordered[index - 1];
      const current = ordered[index];
      if ((current.studyMethodItem ?? 0) > (previous.studyMethodItem ?? 0) + 1 && !previous.completed) {
        issues.push({
          type: "order",
          severity: "error",
          date: current.date,
          taskId: current.id,
          courseId: course.id,
          message: `${current.title} skips earlier required work in ${course.name}.`,
          blockers: [previous.title],
          suggestions: ["Schedule the earlier task first or change this method to a loose/free order mode when that setting exists."],
        });
      }
      if (current.date < previous.date) {
        issues.push({
          type: "order",
          severity: "error",
          date: current.date,
          taskId: current.id,
          courseId: course.id,
          message: `${current.title} is scheduled before ${previous.title}, but this method is strict order.`,
          blockers: [previous.title],
        });
      }
    }
  }

  return issues;
}

function compareIssueSeverity(a: PlannerValidationIssue, b: PlannerValidationIssue) {
  const severityRank = { error: 0, warning: 1, info: 2 };
  return severityRank[a.severity] - severityRank[b.severity] ||
    (a.date ?? "9999-12-31").localeCompare(b.date ?? "9999-12-31") ||
    a.message.localeCompare(b.message);
}

function preferredRevisionDeadline(course: Course, earliestDate: string, hardDeadline: string) {
  return hardDeadline < earliestDate ? earliestDate : hardDeadline;
}

function dateRange(start: string, end: string) {
  if (start > end) return [];

  const dates: string[] = [];
  const cursor = new Date(`${start}T00:00:00`);
  const last = new Date(`${end}T00:00:00`);

  while (cursor <= last) {
    dates.push(dateKey(cursor));
    cursor.setDate(cursor.getDate() + 1);
  }

  return dates;
}

function maxDate(a: string, b: string) {
  return a > b ? a : b;
}

function isDateKey(value: string) {
  const match = value.match(/^(\d{4})-(\d{2})-(\d{2})$/);
  if (!match) return false;
  const year = Number(match[1]);
  const month = Number(match[2]);
  const day = Number(match[3]);
  const date = new Date(year, month - 1, day);
  return date.getFullYear() === year && date.getMonth() === month - 1 && date.getDate() === day;
}

function capacityForDate(date: string, maxDailyHours: number, today: string, todayCapacity: number, rules: CapacityRules) {
  if (rules.holidays.has(date)) return 0;
  const base = date === today ? todayCapacity : maxDailyHours;
  const customHours = rules.customStudyHours.get(date);
  return customHours === undefined ? base : customHours;
}

function migrateRestrictedDays(days?: Array<{ date: string; maxHours: number }>) {
  return (days ?? []).map((day) => ({ date: day.date, hours: day.maxHours }));
}

function isGeneratedRestTask(task: StudyTask) {
  return task.type === "rest" && task.estimatedHours === 0 && (task.autoGenerated || /^rest$/i.test(task.title.trim()));
}

function shouldPreserveTaskForPlanning(task: StudyTask, today: string) {
  if (task.completed || task.type === "exam" || (task.type === "rest" && !isGeneratedRestTask(task))) return true;
  if (task.locked || task.dirty || task.origin === "manual" || (!task.autoGenerated && task.origin !== "generated")) return true;
  return Boolean(task.autoGenerated || task.origin === "generated") && task.date === today;
}

function deadlinePressure(requiredHours: number, availableHours: number) {
  if (requiredHours <= 0) return 0;
  if (availableHours <= 0) return Number.POSITIVE_INFINITY;
  return requiredHours / availableHours;
}

function slug(value: string) {
  return value
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "");
}

function makeTaskStableId(block: WorkBlock) {
  return [
    block.course.id,
    block.method?.id ?? block.type,
    block.methodItem ?? block.itemNumber,
    block.segmentNumber ?? 0,
    slug(block.title),
  ].join("-");
}

function blockKey(courseId: string | undefined, type: TaskType, title: string) {
  return `${courseId ?? "general"}:${type}:${title}`;
}

function taskDeadlineKey(courseId: string, methodId: string, item: number) {
  return `${courseId}:${methodId}:${item}`;
}

function getTaskDeadlineOverrides(tasks: StudyTask[]) {
  const deadlines = new Map<string, string>();
  for (const task of tasks) {
    const deadline = task.customDeadline ?? task.deadline;
    if (!task.courseId || !deadline) continue;
    if (task.studyMethodId && task.studyMethodItem) {
      deadlines.set(taskDeadlineKey(task.courseId, task.studyMethodId, task.studyMethodItem), deadline);
    }
    deadlines.set(blockKey(task.courseId, task.type, task.title), deadline);
  }
  return deadlines;
}

function getTaskOrderOverrides(tasks: StudyTask[]) {
  const orders = new Map<string, number>();
  for (const task of tasks) {
    if (!task.courseId || typeof task.planningOrder !== "number" || !Number.isFinite(task.planningOrder)) continue;
    if (task.studyMethodId && task.studyMethodItem) {
      orders.set(taskDeadlineKey(task.courseId, task.studyMethodId, task.studyMethodItem), task.planningOrder);
    }
    orders.set(blockKey(task.courseId, task.type, task.title), task.planningOrder);
  }
  return orders;
}

function getDependencyIds(course: Course) {
  return course.dependsOnIds?.length ? course.dependsOnIds : course.dependsOnId ? [course.dependsOnId] : [];
}

function getStudyMethods(course: Course): StudyMethod[] {
  return course.studyMethods !== undefined
    ? course.studyMethods
    : [
        {
          id: "lectures",
          name: "Lecture",
          totalItems: course.totalLectures,
          completedItems: course.completedLectures,
          hoursPerItem: course.hoursPerLecture,
          taskType: "lecture",
        },
      ];
}

function getCompletedLectureNumbers(state: StudyState) {
  const result = new Map<string, Set<number>>();

  for (const course of state.courses) {
    const completed = new Set<number>();
    const baseline = Math.min(course.baselineCompletedLectures ?? course.completedLectures, course.totalLectures);
    for (let lecture = 1; lecture <= baseline; lecture += 1) {
      completed.add(lecture);
    }
    result.set(course.id, completed);
  }

  for (const task of state.tasks) {
    if (!task.completed || !task.courseId) continue;
    const completed = result.get(task.courseId);
    if (!completed) continue;
    if (task.type !== "lecture") continue;
    const lectureNumbers = task.lectureNumbers?.length ? task.lectureNumbers : lectureNumbersFromTitle(task.title);
    for (const lecture of lectureNumbers) {
      completed.add(lecture);
    }
  }

  return result;
}

function getCompletedStudyItems(state: StudyState) {
  const result = new Map<string, Map<string, Set<number>>>();

  for (const course of state.courses) {
    const byMethod = new Map<string, Set<number>>();
    for (const method of getStudyMethods(course)) {
      const completed = new Set<number>();
      for (let item = 1; item <= Math.min(method.completedItems, method.totalItems); item += 1) {
        completed.add(item);
      }
      byMethod.set(method.id, completed);
    }
    result.set(course.id, byMethod);
  }

  for (const task of state.tasks) {
    if (!task.completed || !task.courseId || !task.studyMethodId || !task.studyMethodItem) continue;
    const byMethod = result.get(task.courseId);
    const completed = byMethod?.get(task.studyMethodId);
    completed?.add(task.studyMethodItem);
    const titleItem = task.title.match(/(\d+)\s*$/)?.[1];
    if (titleItem) completed?.add(Number(titleItem));
  }

  return result;
}
